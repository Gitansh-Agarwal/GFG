class Solution
{
  public:
    //Function to check if a string is Pangram or not.
    bool checkPangram (string s) {
        // your code here
        vector<int> vec(26, 0);
        for(auto it: s){
            if(it>=65 && it<=90)
                it+=32;
            if(it>=97 && it<=122)
                vec[it - 'a']++;
        }
        for(auto it:vec){
            if(!it)
                return 0;
        }
        return 1;
    }

};

